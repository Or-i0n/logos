def print_logo(lang_name, theme=0):
    """Prints logos of programming languages."""
    chars = logos.get("chars").get(lang_name)[theme]
    logo = logos.get("langs").get(lang_name)

    for i in range(8):
        for j in range(8):
            cell_val = logo[i][j]
            if cell_val != 0:
                print(chars[cell_val - 1], end=" ")
            else:
                print(" ", end=" ")
        print()


